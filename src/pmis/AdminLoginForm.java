    private void loginBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginBtnActionPerformed
        // TODO add your handling code here:
        String emailtxt = emailtxtbx.getText();
        String passtxt = passwordTxtbx.getText();
        String query = "SELECT * FROM AdminAcc WHERE Email = ? AND Password = ?;";
        
        try {
            pst = con.prepareStatement(query);
            pst.setString(1, emailtxt);
            pst.setString(2, passtxt);
            ResultSet rs = pst.executeQuery();
            
            if (rs.next()){
                AdministratorForm administratorForm =  new AdministratorForm();
                administratorForm.setVisible(true);
                dispose();
            }
            else {
                JOptionPane.showMessageDialog(null, "Wrong Email or Password");
            }
        }
        catch (Exception e){
            
        }
    }//GEN-LAST:event_loginBtnActionPerformed

