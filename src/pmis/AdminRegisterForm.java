    private void registerBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerBtnActionPerformed
        // TODO add your handling code here:
        String txtEmail = emailTxtbx.getText();
        String txtPassword = passwordTxt.getText();
        String txtConfPass = confPasswordTxt.getText();
        String query = "INSERT INTO AdminAcc (Email, Password) VALUES (?, ?);"; 
        
        if (txtPassword.equals(txtConfPass)) {
            try {
                pst = con.prepareStatement(query);
                pst.setString(1, txtEmail);
                pst.setString(2, txtPassword);
                int insert = pst.executeUpdate();
                
                if (insert > 0){
                    JOptionPane.showMessageDialog(null, "Registration Successful");
                }
                else {
                    JOptionPane.showMessageDialog(null, "Failed");
                }
                
                AdminLoginForm adminLoginForm = new AdminLoginForm();
                adminLoginForm.setVisible(true);
                dispose();
            }
            catch (Exception e){
                
            }
        }
        else {
            System.out.println("mali");
        }

    }//GEN-LAST:event_registerBtnActionPerformed

